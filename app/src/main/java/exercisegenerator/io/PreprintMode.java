package exercisegenerator.io;

/**
 * Modes for printing solution space.
 */
public enum PreprintMode {

    /**
     * Always print solution space.
     */
    ALWAYS,

    /**
     * Don't print solution space.
     */
    NEVER,

    /**
     * Surround the solution space with the solutionSpace command.
     */
    SOLUTION_SPACE

}
